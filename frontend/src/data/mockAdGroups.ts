export const adGroupsTotals = {
  impressions: mockAdGroups.reduce((sum, g) => sum + g.impressions, 0),
  clicks: mockAdGroups.reduce((sum, g) => sum + g.clicks, 0),
  adUnits: mockAdGroups.reduce((sum, g) => sum + g.adUnits, 0),
  adSpend: mockAdGroups.reduce((sum, g) => sum + g.adSpend, 0),
  adSales: mockAdGroups.reduce((sum, g) => sum + g.adSales, 0),
  get ctr() {
    return this.impressions > 0 ? (this.clicks / this.impressions) * 100 : 0;
  },
  get cvr() {
    return this.clicks > 0 ? (this.adUnits / this.clicks) * 100 : 0;
  },
  get cpc() {
    return this.clicks > 0 ? this.adSpend / this.clicks : 0;
  },
  get roas() {
    return this.adSpend > 0 ? this.adSales / this.adSpend : 0;
  },
  get acos() {
    return this.adSales > 0 ? (this.adSpend / this.adSales) * 100 : 0;
  },
};


// ──────────── Synthetic expansion (Phase 3.3) ────────────
// Procedurally clones existing rows to reach 45 total records
// for realistic pagination demos. Generated at module load.
(() => {
  const base = mockAdGroups.slice();
  const baseLen = base.length;
  if (baseLen === 0) return;
  let nextId = baseLen + 1;
  while (mockAdGroups.length < 45) {
    const src = base[(mockAdGroups.length - baseLen) % baseLen];
    const variance = 0.7 + ((mockAdGroups.length * 37) % 60) / 100;
    const clone: any = { ...src };
    clone.id = "ag-" + nextId;
    if ((clone as any)["name"]) (clone as any)["name"] = (clone as any)["name"] + " #" + nextId;
    for (const k of Object.keys(clone)) {
      const v = (clone as any)[k];
      if (typeof v === "number" && k !== "id" && !k.toLowerCase().includes("date")) {
        (clone as any)[k] = Math.round(v * variance * 100) / 100;
      }
    }
    mockAdGroups.push(clone);
    nextId++;
  }
})();
