// Procedurally clones existing rows to reach 45 total records
// for realistic pagination demos. Generated at module load.
(() => {
  const base = mockKeywords.slice();
  const baseLen = base.length;
  if (baseLen === 0) return;
  let nextId = baseLen + 1;
  while (mockKeywords.length < 45) {
    const src = base[(mockKeywords.length - baseLen) % baseLen];
    const variance = 0.7 + ((mockKeywords.length * 37) % 60) / 100;
    const clone: any = { ...src };
    clone.id = "kw-" + nextId;
    if ((clone as any)["keyword"]) (clone as any)["keyword"] = (clone as any)["keyword"] + " #" + nextId;
    for (const k of Object.keys(clone)) {
      const v = (clone as any)[k];
      if (typeof v === "number" && k !== "id" && !k.toLowerCase().includes("date")) {
        (clone as any)[k] = Math.round(v * variance * 100) / 100;
      }
    }
    mockKeywords.push(clone);
    nextId++;
  }
})();
